module.exports = {};
module.exports.sum = function sum(a, b) {
    return a + b;
}
module.exports.restar = function restar(a, b) {
    return a - b;
}
module.exports.multiplicar = function multiplicar(a, b) {
    return a * b;
}
module.exports.dividir = function dividir(a, b) {
    return a / b;
}


